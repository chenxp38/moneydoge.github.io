package backend1.demo;

public class Contact {
    private String phone;
    private String wechat;

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public void setWechat(String wechat) {
        this.wechat = wechat;
    }

    public String getPhone() {
        return phone;
    }

    public String getWechat() {
        return wechat;
    }
}
